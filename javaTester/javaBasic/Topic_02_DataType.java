package javaBasic;

public class Topic_02_DataType {
	public static void main(String[] args) {
		byte a = 6;
		byte b = 2;
		byte p1 = (byte) (a + b);
		int p2 =a-b;
		int p3=a*b;
		int p4=a/b;
		System.out.println("a+b: " +p1);
		System.out.println("a-b: " +p2);
		System.out.println("a*b: " +p3);
		System.out.println("a/b: " +p4);
	}

}
